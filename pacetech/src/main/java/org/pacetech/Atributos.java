package org.pacetech;

public class Atributos {
    private int opcao;
    private long  metro, km, litro, m3;

    public int getOpcao() {
        return opcao;
    }

    public void setOpcao(int opcao) {
        this.opcao = opcao;
    }

    public long getMetro() {
        return metro;
    }

    public void setMetro(long metro) {
        this.metro = metro;
    }

    public long getKm() {
        return km;
    }

    public void setKm(long km) {
        this.km = km;
    }

    public long getLitro() {
        return litro;
    }

    public void setLitro(long litro) {
        this.litro = litro;
    }

    public long getM3() {
        return m3;
    }

    public void setM3(long m3) {
        this.m3 = m3;
    }
}